function haversine(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371
  const dLat = (lat2 - lat1) * Math.PI / 180
  const dLng = (lng2 - lng1) * Math.PI / 180
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) ** 2
  return Math.round(2 * R * Math.asin(Math.sqrt(a)))
}

export function getGesamtKilometer(verlauf: Standort[]): number {
  if (verlauf.length < 2) return 0
  return verlauf.reduce((sum, s, i) => {
    if (i === 0) return 0
    const prev = verlauf[i - 1]
    return sum + haversine(prev.lat, prev.lng, s.lat, s.lng)
  }, 0)
}

// Woche seit Unix-Epoch — exportiert für standorteDB.ts
export function wocheSeitEpoche(datum: Date): number {
  return Math.floor(datum.getTime() / (7 * 24 * 60 * 60 * 1000))
}

